<?php
/**
 * Component: _wdr_scripts.php
 * Description: Script JS untuk halaman Permintaan Tarik Dana (Withdrawals)
 * Pattern: Composite Pattern - Leaf Component
 * Dependencies: jQuery, iziToast
 */
?>

<script>
    // Konfigurasi Trigger Otomatis dari Flashdata (Server Side)
    <?php if (session()->getFlashdata('success')) : ?>
        iziToast.success({
            timeout: 5000,
            title: 'Berhasil',
            message: '<?= session()->getFlashdata('success') ?>',
            position: 'topCenter'
        });
    <?php endif; ?>

    <?php if (session()->getFlashdata('error')) : ?>
        iziToast.error({
            timeout: 5000,
            title: 'Gagal',
            message: '<?= session()->getFlashdata('error') ?>',
            position: 'topCenter'
        });
    <?php endif; ?>
</script>
